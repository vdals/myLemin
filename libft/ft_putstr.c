#include "libft.h"

int 	ft_putstr(char const *str)
{
	int			sum;
	const char	*ptr;

	ptr = str;
	sum = 0;
	while (*ptr)
	{
		write(1, ptr++, 1);
		sum++;
	}
	return (sum);
}
